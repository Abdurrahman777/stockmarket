package com.ps.sm.dto;

public class StrategyDTO {

    String id;
    String name;
    boolean isSelected = false;

    public StrategyDTO(String id, String name) {
        this.id = id;
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public boolean isSelected() {
        return isSelected;
    }

    public void setSelected(boolean selected) {
        isSelected = selected;
    }
}
